"""### Mini-Batch Stochastic Gradient Descent Implementation

Mini-Batch Gradient Descent (MBGD) is a variation of the Gradient Descent algorithm that splits the training dataset into small batches. It computes the gradient and updates the model parameters for each mini-batch. This approach strikes a balance between pure Stochastic Gradient Descent (which uses one sample) and Batch Gradient Descent (which uses the entire dataset), offering faster convergence than BGD and more stable updates than SGD.

We will implement MBGD with a batch size of 64.
"""

# Reuse X_train_b and y_train_np from the previous SGD setup
# X_train_np = X_train.to_numpy()
# y_train_np = y_train.to_numpy().reshape(-1, 1)
