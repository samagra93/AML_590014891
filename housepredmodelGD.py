from sklearn.metrics import mean_squared_error # Ensure mean_squared_error is imported

# Initialize parameters for Mini-Batch SGD
theta_mbgd = np.random.randn(X_train_b.shape[1], 1) # Random initialization

learning_rate_mbgd = 0.0000000001 # A small learning rate, similar to SGD
n_epochs = 1000 # Number of passes over the entire training dataset
batch_size = 64

m = len(X_train_b)
n_batches = int(np.ceil(m / batch_size))

cost_history_mbgd = [] # To store MSE at the end of each epoch

# Mini-Batch SGD implementation
for epoch in range(n_epochs):
    # Shuffle the data at the beginning of each epoch
    shuffled_indices = np.random.permutation(m)
    X_shuffled = X_train_b[shuffled_indices]
    y_shuffled = y_train_np[shuffled_indices]

    for i in range(n_batches):
        # Get the current mini-batch
        start_idx = i * batch_size
        end_idx = min(start_idx + batch_size, m)
        xi_batch = X_shuffled[start_idx:end_idx]
        yi_batch = y_shuffled[start_idx:end_idx]

        # Calculate gradient for this mini-batch
        gradients_mbgd = 2 * xi_batch.T.dot(xi_batch.dot(theta_mbgd) - yi_batch) / len(xi_batch)

        # Update parameters
        theta_mbgd = theta_mbgd - learning_rate_mbgd * gradients_mbgd

    # Calculate MSE on the full training set at the end of each epoch
    y_pred_train_mbgd = X_train_b.dot(theta_mbgd)
    mse_train_mbgd = mean_squared_error(y_train_np, y_pred_train_mbgd)
    cost_history_mbgd.append(mse_train_mbgd)

print(f"Mini-Batch SGD optimized coefficients (batch size={batch_size}, including intercept):")
